x = input("Введіть додатнє ціле число: ")

if float(x) <= 0 or x.isdigit() == False: x = int(input("Ви ввели від'ємне, не ціле число або число 0. Введіть неохідне число: "))

x = int(x)

y = 1

while y < 1000:
    y += 1
    if x % y == 0 and x != y:
        print("Введене Вами число не є простим")
        break

if x % y != 0: print("Введене Вами число є простим")
